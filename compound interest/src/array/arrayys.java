package array;

public class arrayys {
	public static void main(String[] args) {
		
		System.out.println("Index\tValue");
		
		int joe[] = {32,33,34,35,36};
		
		for(int counter=0; counter<joe.length; counter++) {
			System.out.println(counter + "\t" + joe[counter]);
		}
		
	}
	

}
